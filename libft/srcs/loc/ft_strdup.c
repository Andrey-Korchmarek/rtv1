#include "../../includes/libft.h"

char	*ft_strdup(const char *src)
{
	char	*dup;
	size_t	i;

	if (ft_strplen(src) + 1 == 0)
		return (NULL);
	dup = (char *)malloc(sizeof(char) * (ft_strplen(src) + 1));
	if (!dup)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
