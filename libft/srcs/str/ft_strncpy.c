#include "../../includes/libft.h"

char	*ft_strncpy(char *dest, const char *src, unsigned int n)
{
	size_t i;
	size_t len;

	len = ft_strlen(src);
	i = 0;
	while (i < n && i < len)
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
